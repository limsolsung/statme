interface PixelIconProps {
  name: IconName;
  color: string;
  size?: number;
}

export default function PixelIcon({ name, color, size = 2 }: PixelIconProps) {
  const grid = ICONS[name];
  const totalSize = GRID * size;

  const shadows: string[] = [];
  for (let y = 0; y < GRID; y++) {
    for (let x = 0; x < GRID; x++) {
      if (grid[y][x]) {
        shadows.push(`${x * size}px ${y * size}px 0 ${color}`);
      }
    }
  }

  return (
    <div
      style={{
        width: totalSize,
        height: totalSize,
        position: 'relative',
        flexShrink: 0,
      }}
    >
      <div
        style={{
          position: 'absolute',
          top: 0,
          left: 0,
          width: size,
          height: size,
          boxShadow: shadows.join(','),
        }}
      />
    </div>
  );
}

